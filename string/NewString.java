package string;

public class NewString {

    public static void main(String[] args) {

        // create 2 string Uppercase
        String frist1 =     "JAVA PROGRAMMING";
        String second  =    "java programming";
        System.out.println(frist1.toLowerCase());       //string Lowercase
        System.out.println(second.toUpperCase());       // string Uppercase


        //trim method
        System.out.println(":   before trim   "  +    (frist1));         //string trim method
        System.out.println(":    after trim   "    +    (frist1));
    }
}

